package com.yeoou.tour.model;

public class AssembleContent
{
	private String id;
	private String address;
	private String asstime;
	private String price;
	private String jiesong;
	private String compact;
	public String getCompact()
	{
		return compact;
	}
	public void setCompact(String compact)
	{
		this.compact = compact;
	}
	public String getAddress()
	{
		return address;
	}
	public void setAddress(String address)
	{
		this.address = address;
	}
	public String getAsstime()
	{
		return asstime;
	}
	public void setAsstime(String asstime)
	{
		this.asstime = asstime;
	}
	public String getPrice()
	{
		return price;
	}
	public void setPrice(String price)
	{
		this.price = price;
	}
	public String getId()
	{
		return id;
	}
	public void setId(String id)
	{
		this.id = id;
	}
	public String getJiesong()
	{
		return jiesong;
	}
	public void setJiesong(String jiesong)
	{
		this.jiesong = jiesong;
	}
}
